async function mintNFT() {
	console.log('is it working')
    const accounts = await web3.eth.getAccounts();
	console.log('acounts :: ' ,accounts[0])
    const mintTo = document.getElementById("mintTo").value;
    const tokenURI = document.getElementById("tokenURI").value;

    contract.methods.mint(mintTo, tokenURI).send({ from: accounts[0] })
        .then(result => {
            console.log("NFT Minted:", result);
        })
        .catch(error => {
            console.error("Error minting NFT:", error);
        });
}

async function listNFT() {
    const accounts = await web3.eth.getAccounts();
    const tokenId = document.getElementById("tokenId").value;

    contract.methods.listNFT(tokenId).send({ from: accounts[0], value: web3.utils.toWei('0.01', 'ether') })
        .then(result => {
            console.log("NFT Listed:", result);
        })
        .catch(error => {
            console.error("Error listing NFT:", error);
        });
}

async function buyNFT() {
    const accounts = await web3.eth.getAccounts();
    const tokenId = document.getElementById("buyTokenId").value;
    const recipient = document.getElementById("recipient").value;

    contract.methods.buyNFT(recipient, tokenId).send({ from: accounts[0], value: web3.utils.toWei('0.1', 'ether') })
        .then(result => {
            console.log("NFT Bought:", result);
        })
        .catch(error => {
            console.error("Error buying NFT:", error);
        });
}

async function getListings() {
    contract.methods.getListings().call()
        .then(listings => {
            const listingsElement = document.getElementById("listings");
            listingsElement.innerHTML = '';

            listings.forEach(listing => {
                const li = document.createElement("li");
                li.innerText = `Seller: ${listing.seller}, Token ID: ${listing.tokenId}`;
                listingsElement.appendChild(li);
            });
        })
        .catch(error => {
            console.error("Error fetching listings:", error);
        });
}
